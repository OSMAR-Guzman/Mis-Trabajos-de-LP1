    private void btnELIMINARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnELIMINARActionPerformed
    int fila = tblENTRADA.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un registro!", "AVISO", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        int pregunta = JOptionPane.showConfirmDialog(this, "Borrar Registro?", "ATENCION", JOptionPane.YES_NO_OPTION);
        if (pregunta == JOptionPane.NO_OPTION) {
            return;
        }
        String id = tblENTRADA.getValueAt(tblENTRADA.getSelectedRow(), 0).toString();
        String cadenaSQL = "DELETE FROM EntradaInventario WHERE id = " + id;
        boolean operacion = controlador.actualizar(cadenaSQL);
        System.err.println("Operacion: " + operacion);
        inicializar();
        tabENTRADA.setSelectedIndex(1);
    }//GEN-LAST:event_btnELIMINARActionPerformed

    private void btnMODIFICARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMODIFICARActionPerformed
         int fila = tblENTRADA.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un registro!", "AVISO", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String id = tblENTRADA.getValueAt(tblENTRADA.getSelectedRow(), 0).toString();
        try {
            cadenaSQL = "SELECT * FROM EntradaInventario WHERE id = " + id;
            resultado = controlador.consultar(cadenaSQL);
            if (resultado.next()) {
                txtID.setText(resultado.getString(1));
                recuperarValorCombo(cboPRODUCTO, resultado.getString(2));
                txtCANTIDAD.setText(resultado.getString(3));
                txtFECHA.setDate(formatoFECHA.parse(resultado.getString(4)));

                tabENTRADA.setSelectedIndex(0);
                editar = true;
            }
        } catch (SQLException ex) {
            System.err.println("ERROR SQL EDICION: " + ex);
        } catch (ParseException ex) {
            System.err.println("ERROR PARSE DATOS: " + ex);
        }
        controlador.desconectar();         
    }//GEN-LAST:event_btnMODIFICARActionPerformed

