    private void btnGUARDARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGUARDARActionPerformed
    String id = txtID.getText();
    String fechaentrada = funciones.convertirFecha(txtFECHA);
    String cantidad = txtCANTIDAD.getText();
    String producto = ((ComboBox) cboPRODUCTO.getSelectedItem()).getId();

    if (cantidad.isEmpty() || fechaentrada.isEmpty()) {
        String mensaje = "Todos los datos son obligatorios!";
        JOptionPane.showMessageDialog(this, mensaje);
        return;
    }

    if (!editar) {
        cadenaSQL = "INSERT INTO EntradaInventario VALUES (";
        cadenaSQL += id;
        cadenaSQL += ",";
        cadenaSQL += producto;
        cadenaSQL += ",";
        cadenaSQL += cantidad;
        cadenaSQL += ",'";
        cadenaSQL += fechaentrada;
        cadenaSQL += "')";
    } else {
        cadenaSQL = "UPDATE EntradaInventario SET ";
        cadenaSQL += "id_productos=" + producto;
        cadenaSQL += ",";
        cadenaSQL += "cantidad=" + cantidad;
        cadenaSQL += ",";
        cadenaSQL += "fechaentrada='" + fechaentrada + "'";  // Add single quotes around the date
        cadenaSQL += " WHERE id=" + id;
    }

    boolean operacion = controlador.actualizar(cadenaSQL);
    System.err.println("Operacion: " + operacion);
    inicializar();
    tabENTRADA.setSelectedIndex(1);;
    }//GEN-LAST:event_btnGUARDARActionPerformed

